import mongoose, { Schema, Document, Model } from 'mongoose';

export enum PurchaseOrderStatus {
  PENDING = 'PENDING',
  APPROVED = 'APPROVED',
  CLOSED = 'CLOSED',
  CANCELLED = 'CANCELLED',
  OPEN = 'OPEN',
}

export interface IPurchaseOrder extends Document {
  poId: mongoose.Types.ObjectId;
  tenantId: string;
  branchId: mongoose.Types.ObjectId;
  prNo?: string;
  vendorId?: mongoose.Types.ObjectId | null;
  vendorName?: string;
  createdBy: mongoose.Types.ObjectId;
  approvedBy?: mongoose.Types.ObjectId | null;
  poDate: Date;
  deliveryDate?: Date | null;
  status: PurchaseOrderStatus;
  type: 'STANDARD' | 'SPECIAL';
  totalAmount: number;
  createdAt: Date;
  updatedAt: Date;
}

const PurchaseOrderSchema = new Schema<IPurchaseOrder>(
  {
    tenantId: { type: String, ref: 'Tenant', required: true },
    branchId: { type: Schema.Types.ObjectId, ref: 'Branch', required: true },
    prNo: { type: String, default: null }, // Autogenerated PR Number
    vendorId: { type: Schema.Types.ObjectId, ref: 'Vendor', required: false, default: null },
    vendorName: { type: String, default: null },
    createdBy: { type: Schema.Types.ObjectId, ref: 'User', required: true },
    approvedBy: { type: Schema.Types.ObjectId, ref: 'User', default: null },
    poDate: { type: Date, default: Date.now },
    deliveryDate: { type: Date, default: null },
    status: { type: String, enum: Object.values(PurchaseOrderStatus), default: PurchaseOrderStatus.PENDING },
    type: { type: String, enum: ['STANDARD', 'SPECIAL'], default: 'STANDARD' },
    totalAmount: { type: Number, min: 0, default: 0 },
  },
  {
    timestamps: true,
    collection: 'purchase_orders',
  }
);

PurchaseOrderSchema.virtual('poId').get(function (this: IPurchaseOrder) {
  return this._id;
});

PurchaseOrderSchema.set('toJSON', {
  virtuals: true,
  transform: function (doc, ret: any) {
    ret.poId = ret._id;
    delete ret._id;
    delete ret.__v;
    return ret;
  },
});

PurchaseOrderSchema.index({ tenantId: 1, branchId: 1, status: 1 });
PurchaseOrderSchema.index({ tenantId: 1, vendorId: 1 });
PurchaseOrderSchema.index({ tenantId: 1, poDate: -1 });
PurchaseOrderSchema.index({ tenantId: 1, createdBy: 1 });

export const PurchaseOrder: Model<IPurchaseOrder> = mongoose.model<IPurchaseOrder>('PurchaseOrder', PurchaseOrderSchema);
